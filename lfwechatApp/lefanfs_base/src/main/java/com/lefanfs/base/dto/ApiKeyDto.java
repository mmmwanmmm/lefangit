package com.lefanfs.base.dto;

import java.io.Serializable;

public class ApiKeyDto implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = -8342508427349003639L;

	private String apiKey;
	private String apiSecret;
	private String platform;
	private String apiSource;

	public ApiKeyDto(String apiKey, String apiSecret, String platform, String apiSource) {
		super();
		this.apiKey = apiKey;
		this.apiSecret = apiSecret;
		this.platform = platform;
		this.apiSource = apiSource;
	}

	public String getApiKey() {
		return apiKey;
	}

	public void setApiKey(String apiKey) {
		this.apiKey = apiKey;
	}

	public String getApiSecret() {
		return apiSecret;
	}

	public void setApiSecret(String apiSecret) {
		this.apiSecret = apiSecret;
	}

	public String getPlatform() {
		return platform;
	}

	public void setPlatform(String platform) {
		this.platform = platform;
	}

	public String getApiSource() {
		return apiSource;
	}

	public void setApiSource(String apiSource) {
		this.apiSource = apiSource;
	}

}
