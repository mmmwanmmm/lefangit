package com.lefanfs.base.enums;

/**
 * 绑定第三方平台枚举
 * 
 * @author Daniel
 */
public enum BindPlatformEnum {

	/**
	 * WECHAT/WEIXIN
	 */
	WECHAT,

	/**
	 * QQ
	 */
	QQ,

	/**
	 * WEIBO
	 */
	WEIBO,

	/**
	 * DOUBAN
	 */
	DOUBAN,;

	public static BindPlatformEnum getBindPlatformEnum(String code) {
		try {
			return BindPlatformEnum.valueOf(code.toUpperCase());
		} catch (Exception e) {
			return null;
		}
	}
}
