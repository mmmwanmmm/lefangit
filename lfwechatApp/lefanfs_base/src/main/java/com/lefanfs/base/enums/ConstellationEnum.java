package com.lefanfs.base.enums;

/**
 * 星座枚举
 * 
 * @author Daniel
 */
public enum ConstellationEnum {

	Capricorn(1, "摩羯座"),

	Aquarius(2, "水瓶座"),

	Pisces(3, "双鱼座"),

	Aries(4, "白羊座"),

	Taurus(5, "金牛座"),

	Gemini(6, "双子座"),

	Cancer(7, "巨蟹座"),

	Leo(8, "狮子座"),

	Virgo(9, "处女座"),

	Libra(10, "天秤座"),

	Scorpio(11, "天蝎座"),

	Sagittarius(12, "射手座");

	private ConstellationEnum(int code, String chineseName) {
		this.code = code;
		this.chineseName = chineseName;
	}

	private int code;
	private String chineseName;

	public int getCode() {
		return this.code;
	}

	public String getChineseName() {
		return this.chineseName;
	}
}
